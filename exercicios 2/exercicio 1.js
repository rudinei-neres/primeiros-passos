//1) Você é um amante da natureza e adora fazer trilhas.
//Criar um programa que calcule a velocidade média das trilhas que você realiza. 
//Para isso, devem ser digitados os dados de distância percorrida (quilômetros) e
// tempo que a trilha durou (horas). Fazer então o cálculo da velocidade média e mostrar
// na tela a mensagem "Sua média de velocidade durante essa trilha foi de X km/h", 
//sendo X a velocidade

let distancia , horas , media

distancia = Number(prompt('digite a distancia percorida na trilha em Km: '))
horas = Number(prompt('Digite o digite o tempo decorrido na trila em horas: '))

media = distancia/horas

alert ("A velocidade media no percurso da trilha é: "+ media + "km/h")











